dev = usb.core.find(idVendor=vendor, idProduct=product)
if dev is None:
    print("Keyboard not found")
    exit()
dev.default_timeout = timeout

def detach_device():
    try:
        dev.detach_kernel_driver(0)
        dev.detach_kernel_driver(1)
        dev.set_configuration(bConfigurationValue)
    except Exception as exception:
        print("Failed to detach device: " + str(exception))
        exit()

def reattach_device():
    try:
        usb.util.dispose_resources(dev)
        dev.attach_kernel_driver(0)
        dev.attach_kernel_driver(1)
    except Exception as exception:
        print("Failed to reattach device: " + str(exception))
        exit()

def change_light(button, color):
    msg = prefix + keycodes[button][0] + [0x01, 0x11, 0x03] + keycodes[button][1] + keycodes[button][2] + [0x00] + color
    while len(msg) < 64: #fill msg with zeros to 64 bytes
        msg = msg + [0x00]
    send_packet(msg)

def change_brightness(level):
    msg = prefix + brightness[level][0] + [0x00, 0x06, 0x01, 0x2b, 0x00, 0x00] + brightness[level][1]
    while len(msg) < 64: #fill msg with zeros to 64 bytes
        msg = msg + [0x00]
    send_packet(msg)

def send_packet(msg):
    __open = [0x04, 0x01, 0x00, 0x01] + [0x00]*60
    __close = [0x04, 0x02, 0x00, 0x02] + [0x00]*60
    detach_device()
    dev.ctrl_transfer(0x21, 9, 0x0204, 1, __open)
    dev.ctrl_transfer(0x21, 9, 0x0204, 1, msg)
    try:
        dev.ctrl_transfer(0x21, 9, 0x0204, 1, __close)
    except usb.core.USBError as error:
        reattach_device()
        print(error)
        exit()
    reattach_device()


